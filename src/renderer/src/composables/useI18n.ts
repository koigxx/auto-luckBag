export type MessageKey = keyof (typeof messages)['zh-CN']
type MessageMap = Record<MessageKey, string>
const localizedMessages: Record<Language, MessageMap> = messages

export function useI18n(language: Ref<Language | undefined>) {
  const currentLanguage = computed<Language>(() => language.value || 'zh-CN')

  function t(key: MessageKey, params?: Record<string, string | number>): string {
    let text = localizedMessages[currentLanguage.value][key] || localizedMessages['zh-CN'][key] || key
    if (params) {
      for (const [name, value] of Object.entries(params)) {
        text = text.replace(`{${name}}`, String(value))
      }
    }
    return text
  }

  return {
    currentLanguage,
    t
  }
}
